s= "Ram is a good boy.\n He also studies well" 
#\n = New line 
#skips the line to next line which is known as escape sequence
print(s)

d= "Ram is a good boy.\t He also studies well" 
#\t = Tab Space
#When used inside a string, it inserts a tab space (horizontal tab)
#usually equivalent to 4 or 8 spaces, depending on your terminal or editor settings
print(d)

# Since a single \ is used for n or t command. \\ is used to represent \ in a string

w="Hello\\"
print(w)